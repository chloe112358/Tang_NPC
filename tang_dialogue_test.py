def _clean_npc_reply_text(text):
    """Remove prompt-only Markdown formatting that should never reach players."""
    text = (text or "").strip()
    text = re.sub(r"\*\*(.*?)\*\*", r"\1", text, flags=re.DOTALL)
    text = text.replace("*", "")
    text = text.replace("__", "")
    text = _replace_overused_transition_text(text)
    return text.strip()


def _replace_overused_transition_text(text):
    text = re.sub(r"不過(?=說起|來來來)", "", text)

    if "不過啊" not in text:
        return text

    seed = sum(ord(char) for char in text)
    occurrence = 0

    def replace_match(match):
        nonlocal occurrence
        occurrence += 1
        if occurrence == 1:
            return match.group(0)
        replacement = OVERUSED_TRANSITION_REPLACEMENTS[
            (seed + occurrence) % len(OVERUSED_TRANSITION_REPLACEMENTS)
        ]
        return replacement

    return re.sub(r"不過啊[，,、]?\s*", replace_match, text)


def generate_npc_reply(scene_num, turn_num, user_input):
    """用指定 system prompt 產生 NPC 回覆；錯誤交給呼叫端轉成 fallback。"""
    user_input = (user_input or "").strip()
    if not user_input:
        raise ValueError("玩家輸入不可為空")

    forbidden_color = _detect_forbidden_clothing_request(user_input)
    api_key = _get_api_key_for_scene(scene_num)
    prompt = get_system_prompt(scene_num, turn_num)
    message = _build_user_message(user_input, forbidden_color)

    model_name = (
        os.getenv("GEMINI_MODEL")
        or os.getenv("MODEL_NAME")
        or DEFAULT_MODEL
    )
    with _GENAI_CONFIG_LOCK:
        _ensure_genai_configured(api_key)
        model = genai.GenerativeModel(
            model_name=model_name,
            system_instruction=prompt,
        )
        response = model.start_chat(history=[]).send_message(message)
    text = _clean_npc_reply_text(getattr(response, "text", ""))
    if not text:
        raise RuntimeError("Gemini 未回傳文字")

    if forbidden_color and (
        not _has_clothing_law_warning(text)
        or not _has_convergence(scene_num, turn_num, text)
        or _has_bad_clothing_praise(text)
    ):
        return _build_clothing_law_reply(scene_num, turn_num, forbidden_color)

    return text


def generate_npc_reply_for_branch(branch, turn_num, user_input):
    scene_num, turn_num = get_scene_turn_from_branch(branch, turn_num)
    return generate_npc_reply(scene_num, turn_num, user_input)


