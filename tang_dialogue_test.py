    """互動模式：讓使用者選擇要測試的場景、回合和問題"""
    print("\n" + "=" * 70)
    print("🏮 唐代對話系統測試工具 🏮")
    print("=" * 70)
    print("\n可用場景：")
    print("  1. 教坊聲學探秘（雷班主 - 舞台總監）")
    print("     - Turn 1: 音量與共鳴")
    print("     - Turn 2: 降噪與信噪比")
    print("     - Turn 3: 樂團同步")
    print("\n  2. 西市食衣探秘（安掌櫃 - 酒樓布莊老闆）")
    print("     - Turn 1: 食物烹調與保鮮")
    print("     - Turn 2: 服飾材質與剪裁")
    print("     - Turn 3: 服飾顏色與社會階級")
    print("\n  3. 東市住行探秘（裴掌櫃 - 商行總管）")
    print("     - Turn 1: 車輿減震技術")
    print("     - Turn 2: 高密度倉儲與防火")
    print("     - Turn 3: 預製結構與榫卯建材")
    print("\n輸入 'quit' 退出\n")
    
    while True:
        # 選擇場景
        scene_input = input("請選擇場景 (1/2/3): ").strip()
        
        if scene_input.lower() == 'quit':
            print("\n👋 測試結束")
            break
        
        try:
            scene_num = int(scene_input)
            if scene_num not in [1, 2, 3]:
                print("❌ 請輸入 1, 2, 或 3\n")
                continue
        except ValueError:
            print("❌ 請輸入有效的數字\n")
            continue
        
        # 選擇回合
        turn_input = input("請選擇回合 (1/2/3): ").strip()
        
        try:
            turn_num = int(turn_input)
            if turn_num not in [1, 2, 3]:
                print("❌ 請輸入 1, 2, 或 3\n")
                continue
        except ValueError:
            print("❌ 請輸入有效的數字\n")
            continue
        
        # 顯示預設問題
        print(f"\n場景 {scene_num} - 回合 {turn_num} 的預設問題：")
        for key, question in PRESET_QUESTIONS[(scene_num, turn_num)].items():
            # 截斷過長的問題顯示
            display_q = question if len(question) <= 60 else question[:57] + "..."
            print(f"  {key}. {display_q}")
        print("  或輸入 'custom' 自訂問題")
        
        # 選擇問題
        question_input = input("\n請選擇問題 (A/B/custom): ").strip()
        
        if question_input.lower() == 'custom':
            custom_q = input("請輸入你的問題：").strip()
            if custom_q:
                test_dialogue(scene_num, turn_num, custom_question=custom_q)
        elif question_input.upper() in ['A', 'B']:
            test_dialogue(scene_num, turn_num, question_choice=question_input.upper())
        else:
            print("❌ 無效的選項\n")
            continue
        
        print("\n")


